import random

def 猜数字游戏():
    print("我想了一个1-100之间的数字，猜猜是多少？")
    秘密数字 = random.randint(1, 100)
    尝试次数 = 0
    猜对了 = False
    
    while not 猜对了:
        try:
            猜测 = int(input("你的猜测（1-100）："))
        except ValueError:
            print("请输入1-100之间的整数！")
            continue
        
        尝试次数 += 1
        
        if 猜测 < 1 or 猜测 > 100:
            print("请输入1-100之间的数字！")
        elif 猜测 < 秘密数字:
            print("太小了！再试一次。")
        elif 猜测 > 秘密数字:
            print("太大了！再试一次。")
        else:
            猜对了 = True
            print(f"🎉 恭喜！你猜对了！数字是{秘密数字}")
            print(f"你用了{尝试次数}次猜中")
    
    return 尝试次数

# 运行游戏
尝试次数 = 猜数字游戏()

# 根据尝试次数给出评价
if 尝试次数 <= 5:
    print("太厉害了！你是猜数字高手！")
elif 尝试次数 <= 10:
    print("不错哦！继续加油！")
else:
    print("多玩几次就会更熟练的！")

